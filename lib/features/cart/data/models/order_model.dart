class OrderModel {
  final String fullName;
  final String email;
  final String phone;
  final String address;
  final String city;
  final String productName;
  final String productAmount;

  OrderModel({
    required this.fullName,
    required this.email,
    required this.phone,
    required this.address,
    required this.city,
    required this.productName,
    required this.productAmount,
  });
}
